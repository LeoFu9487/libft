#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		ct[2];

	ct[0] = -1;
	ct[1] = -1;
	while (s[++ct[0]])
		if (s[ct[0]] == c)
			ct[1] = ct[0];
	if (c == 0)
		return ((char*)s + ct[0]);
	if (ct[1] == -1)
		return (0);
	return ((char*)s + ct[1]);
}
